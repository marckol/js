var PantoneYearColors = {
    COLORS: PANTONE_YEAR_COLORS,
	NAMES : PANTONE_YEAR_COLOR_NAMES,
	YEARS : PANTONE_YEARS,
	STRING_YEARS : PANTONE_STRING_YEARS,
	getColor:function(c) {
	    if (typeof c === "string") {
		    return PantoneYearColors.COLORS[c];
		} else if (Number.isInteger(c)) {
		    return PantoneYearColors.COLORS[c.toString()];
		}
	},
	getColorName:function(c) {
	    if (Number.isInteger(c)) {
		    return PantoneYearColors.NAMES[c];
		}
	},
	getStringYear:function(c) {
	    if (Number.isInteger(c)) {
		    return PantoneYearColors.STRING_YEARS[c];
		}
	},
	getYear:function(c) {
	    if (Number.isInteger(c)) {
		    return PantoneYearColors.YEARS[c];
		}
	},
	getColors:function() {
	    return PantoneYearColors.COLORS;
	},
	getColorsList:function() {
	    return PANTONE_YEAR_COLORS_LIST;
	}
};

